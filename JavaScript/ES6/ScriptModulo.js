/*
Para agregar un script de tipo módulo en HTML y asignarle el archivo fuente index.js, 
debes agregar la siguiente línea de código dentro de la sección <body> del documento HTML:
*/

<script type="module" src="index.js"></script>

/*
Este código le indica al navegador que cargue el archivo index.js como un módulo y que 
se utilice la nueva funcionalidad de importación y exportación de ES6. La etiqueta script 
debe estar dentro de la sección <body> del documento HTML para que pueda acceder a 
los elementos del DOM.
*/

// Código completo HTML:

/*
<html>
  <body>
    <script type="module" src="index.js"></script>
  </body>
</html>
*/